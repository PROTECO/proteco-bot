class InfoProteco:
    """
    Class which contains information about the PROTECO program.
    """
    @staticmethod
    def objetivo() -> str:
        """
        Regresa el objetivo del bot.
        Returns:
        --------
        str
            Objetivo del bot.
        """
        return ('En PROTECO nos posicionamos como el programa estudiantil de referencia en la (UNAM), formando no '
        'solo especialistas en tecnología, sino también  enfatizamos el desarrollo de la habilidad para transmitir '
        'conocimientos de manera efectiva.\n\nSuperamos los límites tradicionales, enfocándonos en la difusión efectiva '
        'y accesible del saber tecnológico.\n\nNos enorgullece preparar a nuestros participantes para ser motores de cambio, '
        'elevando la calidad educativa y extendiendo nuestro impacto más allá de la comunidad universitaria.\n\nCreemos en'
        ' el aprendizaje práctico y colaborativo como medio para adquirir y transmitir conocimientos técnicos, promoviendo '
        ' la innovación y la contribución social.')

    @staticmethod
    def mision() -> str:
        """
        Regresa la misión del programa Proteco.
        Returns:
        --------
        str
            Misión del programa.
        """
        return ('Nuestra misión es fomentar una cultura de formación permanente, dinámica '
        'y participativa en la UNAM, donde estudiantes sobresalientes se convierten en catalizadores del conocimiento. '
        '\n\nA través de nuestro programa de becarios, creado por y para estudiantes desde el año 2000, buscamos complementar '
        'la formación académica, personal y profesional de los participantes, desarrollando tanto soft skills como hard '
        'skills, especialmente en el ámbito docente.\n\nNos comprometemos a elevar la calidad de los contenidos, las '
        'técnicas de enseñanza-aprendizaje, los métodos de investigación y los servicios académicos de apoyo, compartiendo '
        'nuestro aprendizaje tanto dentro de la comunidad UNAM como con el público en general.')